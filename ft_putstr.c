#include"ft_printf.h"

void	ft_putstr(char *s, int *j)
{
	int	i;

	i = 0;
	if (!s)
	{
		*j += write(1, "(null)", 6);
		return ;
	}
	while (s[i] != '\0' )
	{
		ft_putchar(s[i], j);
		i++;
	}
}
